const ensureUrl = (v: string) => (/^https?:\/\//i.test(v) ? v : `https://${v}`);

/** Profile social row: an icon per network that has a URL, linking out. */
export function SocialLinks({ links }: { links: Record<string, unknown> }) {
  const present = NETWORKS.map((n) => {
    const v = links[n.key];
    return typeof v === "string" && v.trim() ? { ...n, url: ensureUrl(v.trim()) } : null;
  }).filter((n): n is (typeof NETWORKS)[number] & { url: string } => n !== null);

  if (present.length === 0) return null;

  return (
    <div className="mt-4 flex items-center gap-4">
      {present.map((n) => (
        <a
          key={n.key}
          href={n.url}
          target="_blank"
          rel="noopener noreferrer"
          aria-label={n.label}
          title={n.label}
          style={{ color: n.color }}
          className="transition-opacity hover:opacity-70"
        >
          <svg viewBox="0 0 24 24" width="26" height="26" fill="currentColor" aria-hidden="true">
            <path d={n.path} />
          </svg>
        </a>
      ))}
    </div>
  );
}
